					url: `${this.$storage.get('sessionTable')}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
						if(((json.paotuizhanghao!=''&&json.paotuizhanghao) || json.paotuizhanghao==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.paotuizhanghao = json.paotuizhanghao
							this.ro.paotuizhanghao = true;
						}
						if(((json.paotuixingming!=''&&json.paotuixingming) || json.paotuixingming==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.paotuixingming = json.paotuixingming
							this.ro.paotuixingming = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
				this.peisongzhuangtaiOptions = "已送达,配送中".split(',')
			
			},
			// 多级联动参数

			info(id) {
				this.$http({
					url: `dingdanpeisong/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.shangpintupian!=null) {
						this.ruleForm.shangpintupian = this.ruleForm.shangpintupian.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							
							await this.$http({
								url: `dingdanpeisong/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.dingdanpeisongCrossAddOrUpdateFlag = false;
											this.parent.search();
											this.parent.contentStyleChange();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.dingdanpeisongCrossAddOrUpdateFlag = false;
				this.parent.contentStyleChange();
			},
			shangpintupianUploadChange(fileUrls) {
				this.ruleForm.shangpintupian = fileUrls;
			},
		}
	};
</script>
