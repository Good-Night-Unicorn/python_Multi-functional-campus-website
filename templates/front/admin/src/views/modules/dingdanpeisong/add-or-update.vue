							this.ruleForm.goumaizhanghao = obj[o];
							this.ro.goumaizhanghao = true;
							continue;
						}
						if(o=='goumaixingming'){
							this.ruleForm.goumaixingming = obj[o];
							this.ro.goumaixingming = true;
							continue;
