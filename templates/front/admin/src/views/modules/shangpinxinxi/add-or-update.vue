<script>
	import { 
		isNumber,
		isIntNumer,
	} from "@/utils/validate";
	export default {
		data() {
			var validateNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isNumber(value)) {
					callback(new Error("请输入数字"));
				} else {
					callback();
				}
			};
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					shangpinbianma : false,
					shangpinmingcheng : false,
					shangpinfenlei : false,
					shangpintupian : false,
					shangpinguige : false,
					shangpinjiage : false,
					shangpinxiangqing : false,
					shangpinzhuangtai : false,
					yonghuzhanghao : false,
					yonghuxingming : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					storeupnum : false,
				},
			
				ruleForm: {
					shangpinbianma: this.getUUID(),
					shangpinmingcheng: '',
					shangpinfenlei: '',
					shangpintupian: '',
					shangpinguige: '',
					shangpinjiage: '',
					shangpinxiangqing: '',
					shangpinzhuangtai: '未出售',
					yonghuzhanghao: '',
					yonghuxingming: '',
					clicktime: '',
				},
				shangpinfenleiOptions: [],
				shangpinzhuangtaiOptions: [],

				rules: {
					shangpinbianma: [
					],
					shangpinmingcheng: [
					],
					shangpinfenlei: [
					],
					shangpintupian: [
					],
					shangpinguige: [
					],
					shangpinjiage: [
						{ validator: validateNumber, trigger: 'blur' },
					],
					shangpinxiangqing: [
					],
					shangpinzhuangtai: [
					],
					yonghuzhanghao: [
					],
					yonghuxingming: [
					],
					clicktime: [
					],
					clicknum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					discussnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					storeupnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
				},
			};
		},
		props: ["parent"],
		computed: {



		},
		components: {
		},
		created() {
		},
		methods: {
			// 下载
			download(file){
				window.open(`${file}`)
			},
			// 初始化
			init(id,type) {
				if (id) {
					this.id = id;
					this.type = type;
				}
				if(this.type=='info'||this.type=='else'||this.type=='msg'){
					this.info(id);
				}else if(this.type=='logistics'){
					for(let x in this.ro) {
						this.ro[x] = true
					}
					this.logistics=false;
					this.info(id);
				}else if(this.type=='cross'){
					var obj = this.$storage.getObj('crossObj');
					for (var o in obj){
						if(o=='shangpinbianma'){
							this.ruleForm.shangpinbianma = obj[o];
							this.ro.shangpinbianma = true;
							continue;
						}
						if(o=='shangpinmingcheng'){
							this.ruleForm.shangpinmingcheng = obj[o];
							this.ro.shangpinmingcheng = true;
							continue;
						}
						if(o=='shangpinfenlei'){
							this.ruleForm.shangpinfenlei = obj[o];
							this.ro.shangpinfenlei = true;
							continue;
						}
						if(o=='shangpintupian'){
							this.ruleForm.shangpintupian = obj[o];
							this.ro.shangpintupian = true;
							continue;
						}
						if(o=='shangpinguige'){
							this.ruleForm.shangpinguige = obj[o];
							this.ro.shangpinguige = true;
							continue;
						}
						if(o=='shangpinjiage'){
							this.ruleForm.shangpinjiage = obj[o];
							this.ro.shangpinjiage = true;
							continue;
						}
						if(o=='shangpinxiangqing'){
							this.ruleForm.shangpinxiangqing = obj[o];
							this.ro.shangpinxiangqing = true;
							continue;
						}
						if(o=='shangpinzhuangtai'){
							this.ruleForm.shangpinzhuangtai = obj[o];
							this.ro.shangpinzhuangtai = true;
							continue;
						}
						if(o=='yonghuzhanghao'){
							this.ruleForm.yonghuzhanghao = obj[o];
							this.ro.yonghuzhanghao = true;
							continue;
						}
						if(o=='yonghuxingming'){
							this.ruleForm.yonghuxingming = obj[o];
							this.ro.yonghuxingming = true;
							continue;
						}
						if(o=='clicktime'){
							this.ruleForm.clicktime = obj[o];
							this.ro.clicktime = true;
							continue;
						}
						if(o=='clicknum'){
							this.ruleForm.clicknum = obj[o];
							this.ro.clicknum = true;
							continue;
						}
						if(o=='discussnum'){
							this.ruleForm.discussnum = obj[o];
							this.ro.discussnum = true;
							continue;
						}
						if(o=='storeupnum'){
							this.ruleForm.storeupnum = obj[o];
							this.ro.storeupnum = true;
							continue;
						}
					}
					this.ruleForm.shangpinzhuangtai = '未出售'; 				}
				// 获取用户信息
				this.$http({
					url: `${this.$storage.get('sessionTable')}/session`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						var json = data.data;
						if(this.$storage.get("role")!="管理员") {
							this.ro.shangpinzhuangtai = true;
						}
						if(((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.yonghuzhanghao = json.yonghuzhanghao
							this.ro.yonghuzhanghao = true;
						}
						if(((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0) && this.$storage.get("role")!="管理员"){
							this.ruleForm.yonghuxingming = json.yonghuxingming
							this.ro.yonghuxingming = true;
						}
					} else {
						this.$message.error(data.msg);
					}
				});
				this.$http({
					url: `option/shangpinfenlei/shangpinfenlei`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.shangpinfenleiOptions = data.data;
					} else {
						this.$message.error(data.msg);
					}
				});
				this.shangpinzhuangtaiOptions = "已出售,未出售".split(',')
			
			},
			// 多级联动参数

			info(id) {
				this.$http({
					url: `shangpinxinxi/info/${id}`,
					method: "get"
				}).then(({ data }) => {
					if (data && data.code === 0) {
						this.ruleForm = data.data;
						//解决前台上传图片后台不显示的问题
						let reg=new RegExp('../../../upload','g')//g代表全部
						this.ruleForm.shangpinxiangqing = this.ruleForm.shangpinxiangqing.replace(reg,'../../../django85gv12pu/upload');
					} else {
						this.$message.error(data.msg);
					}
				});
			},

			// 提交
			async onSubmit() {
					if(this.ruleForm.shangpinbianma) {
						this.ruleForm.shangpinbianma = String(this.ruleForm.shangpinbianma)
					}
					if(this.ruleForm.shangpintupian!=null) {
						this.ruleForm.shangpintupian = this.ruleForm.shangpintupian.replace(new RegExp(this.$base.url,"g"),"");
					}
					var objcross = this.$storage.getObj('crossObj');
					await this.$refs["ruleForm"].validate(async valid => {
						if (valid) {
							if(this.type=='cross'){
								var statusColumnName = this.$storage.get('statusColumnName');
								var statusColumnValue = this.$storage.get('statusColumnValue');
								if(statusColumnName!='') {
									var obj = this.$storage.getObj('crossObj');
									if(statusColumnName && !statusColumnName.startsWith("[")) {
										for (var o in obj){
											if(o==statusColumnName){
												obj[o] = statusColumnValue;
											}
										}
										var table = this.$storage.get('crossTable');
										await this.$http({
											url: `${table}/update`,
											method: "post",
											data: obj
										}).then(({ data }) => {});
									}
								}
							}
							
							await this.$http({
								url: `shangpinxinxi/${!this.ruleForm.id ? "save" : "update"}`,
								method: "post",
								data: this.ruleForm
							}).then(async ({ data }) => {
								if (data && data.code === 0) {
									this.$message({
										message: "操作成功",
										type: "success",
										duration: 1500,
										onClose: () => {
											this.parent.showFlag = true;
											this.parent.addOrUpdateFlag = false;
											this.parent.shangpinxinxiCrossAddOrUpdateFlag = false;
											this.parent.search();
											this.parent.contentStyleChange();
										}
									});
								} else {
									this.$message.error(data.msg);
								}
							});
						}
					});
			},
			// 获取uuid
			getUUID () {
				return new Date().getTime();
			},
			// 返回
			back() {
				this.parent.showFlag = true;
				this.parent.addOrUpdateFlag = false;
				this.parent.shangpinxinxiCrossAddOrUpdateFlag = false;
				this.parent.contentStyleChange();
			},
			shangpintupianUploadChange(fileUrls) {
				this.ruleForm.shangpintupian = fileUrls;
			},
		}
	};
</script>
